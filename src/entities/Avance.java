package entities;

public class Avance {
private int numa;
private String mois;
private float  montant;
private String nume;
private int acceptationa;
public Avance() {
	super();
}
public int getNuma() {
	return numa;
}
public void setNuma(int numa) {
	this.numa = numa;
}

public float getMontant() {
	return montant;
}
public void setMontant(float montant) {
	this.montant = montant;
}

public int getAcceptationa() {
	return acceptationa;
}
public void setAcceptationa(int acceptationa) {
	this.acceptationa = acceptationa;
}
public String getMois() {
	return mois;
}
public void setMois(String mois) {
	this.mois = mois;
}
public String getNume() {
	return nume;
}
public void setNume(String nume) {
	this.nume = nume;
}



}
