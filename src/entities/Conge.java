package entities;

public class Conge {
	private int numc;
	private String datedc;
	private String datefc;
	private String typec;
	private int acceptationc;
	private String nume;
	public Conge() {
		super();
	}
	public int getNumc() {
		return numc;
	}
	public void setNumc(int numc) {
		this.numc = numc;
	}
	public String getDatedc() {
		return datedc;
	}
	public void setDatedc(String datedc) {
		this.datedc = datedc;
	}
	public String getDatefc() {
		return datefc;
	}
	public void setDatefc(String datefc) {
		this.datefc = datefc;
	}
	public String getTypec() {
		return typec;
	}
	public void setTypec(String typec) {
		this.typec = typec;
	}
	public int getAcceptationc() {
		return acceptationc;
	}
	public void setAcceptationc(int acceptationc) {
		this.acceptationc = acceptationc;
	}
	public String getNume() {
		return nume;
	}
	public void setNume(String nume) {
		this.nume = nume;
	}


}
